// Merge function to combine insights with their content blocks
export function mergeInsightsWithBlocks(baseInsights: BlogPost[]): BlogPost[] {
  return baseInsights.map(insight => {
    const extendedInsight = insightsWithBlocks.find(ext => ext.id === insight.id);
    if (extendedInsight?.content_blocks) {
      return {
        ...insight,
        content_blocks: extendedInsight.content_blocks,
      };
    }
    return insight;
  });
}